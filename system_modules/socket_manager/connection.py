from enum import Flag


class ConnectionStatus(Flag):
    """
    Connection status flags
    """
    OFFLINE = 0B00000000
    AWAITING_CONNECTION = 0B00000001
    CONNECTING = 0B00000010
    CONNECTED = 0B00000100
    DISCONNECTING = 0B00001000
    DISCONNECTED = 0B00010000
    ERROR = 0B00100000
    CLOSING = 0B01000000
    CLOSED = 0B10000000

